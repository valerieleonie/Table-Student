    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblStudent.getSelectedRow();
        DefaultTableModel tableModel = (DefaultTableModel) tblStudent.getModel();
        tableModel.setValueAt(txtName.getText(), row, 0);
        tableModel.setValueAt(txtEmail.getText(), row, 2);
        tableModel.setValueAt(cboGender.getSelectedItem(), row, 1);
        tableModel.setValueAt(spiYear.getValue(), row, 3);
        tableModel.setValueAt(spiTuition.getValue(), row, 4);
        tableModel.setValueAt(chkActive.isSelected(), row, 5);
    }//GEN-LAST:event_btnUpdateActionPerformed

