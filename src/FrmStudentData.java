    private void tableSelectionListener() {
        ListSelectionListener listener = new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                int row = tblStudent.getSelectedRow();
                if (row >= 0) {
                    txtName.setText(tblStudent.getValueAt(row, 0).toString());
                    txtEmail.setText(tblStudent.getValueAt(row, 2).toString());
                    cboGender.setSelectedItem(tblStudent.getValueAt(row, 1));
                    spiYear.setValue(tblStudent.getValueAt(row, 3));
                    spiTuition.setValue(tblStudent.getValueAt(row, 4));
                    chkActive.setSelected(
                            Boolean.valueOf(tblStudent.getValueAt(row, 5).toString()));
                }
            }
        };
        tblStudent.getSelectionModel().setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tblStudent.getSelectionModel().addListSelectionListener(listener);

    }

    private void tableInitData() {

        Object data[][] = {
            {"Maikel", "Male", "mkdika@gmail.com", 2007, 56000, true}
        };
        DefaultTableModel tableModel = (DefaultTableModel) tblStudent.getModel();
        for (Object o[] : data) {
            tableModel.addRow(o);
        }
    }

